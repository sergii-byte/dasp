def diagram(title, blocks, footnote):
    p.H2(title)
    x, y = 18, p.get_y()
    p.set_fill_color(*LIGHT); p.set_draw_color(*LINE)
    # compute height
    p.set_font("D", "", 8.5)
    h = 4
    for b in blocks:
        h += 9 if b[0] == "top" else 8
    p.rect(x, y, p.cw, h, "F")
    cy = y + 2.5
    for kind, txt in blocks:
        if kind == "top":
            p.set_font("D", "B", 9); p.set_text_color(*NAVY)
            p.set_xy(x + 3, cy); p.multi_cell(p.cw - 6, 4.3, txt, new_x="LMARGIN", new_y="NEXT"); cy += 9
        elif kind == "arrow":
            p.set_font("D", "", 8); p.set_text_color(*STEEL)
            p.set_xy(x + 3, cy); p.cell(p.cw - 6, 4, txt, align="C"); cy += 8
        else:
            p.set_font("D", "B", 8.5); p.set_text_color(*INK)
            p.set_xy(x + 3, cy); p.multi_cell(p.cw - 6, 4, txt, new_x="LMARGIN", new_y="NEXT"); cy += 8
    p.set_y(y + h + 2)
    p.set_font("D", "I", 8); p.set_text_color(*GREY); p.set_x(18)
    p.multi_cell(p.cw, 4.2, footnote, new_x="LMARGIN", new_y="NEXT"); p.ln(2)

