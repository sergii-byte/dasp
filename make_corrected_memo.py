FD = "/usr/share/fonts/truetype/dejavu"
NAVY = (20, 38, 71)
STEEL = (70, 90, 120)
GREY = (95, 95, 95)
LIGHT = (243, 245, 249)
LINE = (205, 210, 220)
ACC = (140, 25, 25)
INK = (28, 28, 28)
CONN = (95, 115, 145)
BOXF = (236, 240, 247)


class PDF(FPDF):
    def __init__(self):
        super().__init__("P", "mm", "A4")
        self.set_auto_page_break(True, 18)
        self.add_font("D", "", f"{FD}/DejaVuSans.ttf")
        self.add_font("D", "B", f"{FD}/DejaVuSans-Bold.ttf")
        self.add_font("D", "I", f"{FD}/DejaVuSans.ttf")
        self.set_margins(18, 18, 18)
        self.cw = 174

    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("D", "", 7.5)
        self.set_text_color(*GREY)
        self.set_y(9)
        self.cell(0, 5, "SBLC LAW FIRM   ·   Проект STRIKER · L-259U")
        self.set_x(18)
        self.cell(0, 5, "Конфиденциально", align="R")
        self.set_draw_color(*LINE)
        self.line(18, 15, 192, 15)
        self.set_y(20)

    def footer(self):
        if self.page_no() == 1:
            return
        self.set_y(-14)
        self.set_draw_color(*LINE)
        self.line(18, self.get_y(), 192, self.get_y())
        self.set_font("D", "", 7.5)
        self.set_text_color(*GREY)
        self.cell(0, 8, "Меморандум о структуре проекта · для инвесторов")
        self.set_x(18)
        self.cell(0, 8, f"{self.page_no()}", align="R")

    def H1(self, num, text):
        if self.get_y() > 250:
            self.add_page()
        self.ln(2)
        self.set_font("D", "B", 14)
        self.set_text_color(*NAVY)
        self.set_x(18)
        label = f"{num}   {text}" if num else text
        self.multi_cell(self.cw, 7.5, label, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*NAVY)
        self.line(18, self.get_y() + 1, 192, self.get_y() + 1)
        self.ln(3.5)

    def H2(self, text):
        if self.get_y() > 255:
            self.add_page()
        self.ln(1.5)
        self.set_font("D", "B", 10.5)
        self.set_text_color(*NAVY)
        self.set_x(18)
        self.multi_cell(self.cw, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def P(self, text, size=9.5):
        self.set_font("D", "", size)
        self.set_text_color(*INK)
        self.set_x(18)
        self.multi_cell(self.cw, 5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1.2)

    def B(self, text):
        self.set_font("D", "", 9.5)
        self.set_text_color(*INK)
        self.set_x(18)
        self.cell(5, 5, "—")
        self.set_x(23)
        self.multi_cell(self.cw - 5, 5, text, new_x="LMARGIN", new_y="NEXT")

    def note(self, title, text):
        self.ln(0.5)
        x, y = 18, self.get_y()
        head = title
        self.set_font("D", "B", 8.5)
        hh = 5
        self.set_font("D", "", 8.7)
        lines = self.multi_cell(self.cw - 7, 4.6, text, dry_run=True, output="LINES")
        h = hh + len(lines) * 4.6 + 2.5
        self.set_fill_color(*LIGHT)
        self.rect(x, y, self.cw, h, "F")
        self.set_line_width(0.9)
        self.set_draw_color(*NAVY)
        self.line(x, y, x, y + h)
        self.set_line_width(0.2)
        self.set_xy(x + 3.5, y + 1.6)
        self.set_font("D", "B", 8.7)
        self.set_text_color(*NAVY)
        self.cell(self.cw - 7, 4.2, head)
        self.set_xy(x + 3.5, y + 1.6 + hh)
        self.set_font("D", "", 8.7)
        self.set_text_color(60, 60, 60)
        self.multi_cell(self.cw - 7, 4.6, text, new_x="LMARGIN", new_y="NEXT")
        self.set_y(y + h + 1.5)

    def table(self, headers, widths, rows, fs=8):
        sc = self.cw / sum(widths)
        widths = [w * sc for w in widths]
        def hdr():
            self.set_font("D", "B", fs)
            self.set_fill_color(*NAVY)
            self.set_text_color(255, 255, 255)
            self.set_x(18)
            for w, h in zip(widths, headers):
                self.cell(w, 6.5, " " + h, fill=True, border=0)
            self.ln(6.5)
        hdr()
        fill = False
        for row in rows:
            self.set_font("D", "", fs)
            lines = [self.multi_cell(w - 2, 4.3, str(c), dry_run=True, output="LINES") for w, c in zip(widths, row)]
            h = max(len(l) for l in lines) * 4.3 + 2
            if self.get_y() + h > 272:
                self.add_page()
                hdr()
            x, y = 18, self.get_y()
            self.set_fill_color(*(LIGHT if fill else (255, 255, 255)))
            for w, c in zip(widths, row):
                cx, cy = self.get_x(), self.get_y()
                self.rect(cx, cy, w, h, "F")
                self.set_xy(cx + 1, cy + 1)
                self.set_text_color(*INK)
                self.multi_cell(w - 2, 4.3, str(c), new_x="RIGHT", new_y="TOP", max_line_height=4.3)
                self.set_xy(cx + w, cy)
            self.set_draw_color(*LINE)
            self.line(x, y + h, x + sum(widths), y + h)
            self.set_xy(x, y + h)
            fill = not fill
        self.ln(2)

    # -------- graphical structure diagram --------
    def structure(self, stage, compact=False, show_plant=True):
        cx = 105
        sub_size = 7.0

        def box(x, y, w, h, title, sub, dashed=False):
            self.set_fill_color(*((255, 255, 255) if dashed else BOXF))
            self.set_draw_color(*NAVY)
            self.set_line_width(0.5)
            if dashed:
                self.set_dash_pattern(dash=1.3, gap=1.3)
            self.rect(x, y, w, h, "DF")
            if dashed:
                self.set_dash_pattern()
            self.set_line_width(0.2)
            self.set_font("D", "B", 8.2)
            self.set_text_color(*NAVY)
            self.set_xy(x, y + 1.8)
            self.cell(w, 3.8, title, align="C")
            if sub:
                self.set_font("D", "", sub_size)
                self.set_text_color(75, 75, 75)
                self.set_xy(x + 1.5, y + 5.7)
                self.multi_cell(w - 3, 3.3, sub, align="C", new_x="LMARGIN", new_y="NEXT")

        def vconn(x, y1, y2, tip=False):
            self.set_draw_color(*CONN)
            self.set_line_width(0.4)
            self.line(x, y1, x, y2)
            if tip:
                self.line(x - 1.2, y2 - 1.8, x, y2)
                self.line(x + 1.2, y2 - 1.8, x, y2)
            self.set_line_width(0.2)

        def hconn(x1, x2, y):
            self.set_draw_color(*CONN)
            self.set_line_width(0.4)
            self.line(x1, y, x2, y)
            self.set_line_width(0.2)

        def label(x, y, txt):
            self.set_font("D", "", 6.7)
            self.set_text_color(*GREY)
            self.set_xy(x, y)
            self.cell(34, 3, txt)

        y = self.get_y() + 1
        box(72, y, 66, 8, "ИНВЕСТОРЫ", "")
        vconn(cx, y + 8, y + 12, tip=True)
        hh = 11 if compact else 12
        box(33, y + 12, 144, hh, "ХОЛДИНГ — Великобритания (рекомендуется)",
            "владеет проектом и правами на разработку; распределяет прибыль инвесторам")
        busy = y + 12 + hh
        vconn(cx, busy, busy + 3)
        hconn(63, 147, busy + 3)
        vconn(63, busy + 3, busy + 7, tip=True)
        vconn(147, busy + 3, busy + 7, tip=True)
        label(cx - 16, busy + 0.2, "владеет 100% обеих компаний")
        oy = busy + 7
        oh = 13 if compact else 15
        if stage == 1:
            box(24, oy, 78, oh, "PrincipalCo — Болгария",
                "производство и закупки; владеет проектом и правами на разработку")
            box(108, oy, 78, oh, "SalesCo — Болгария",
                "военно-экспортная лицензия; продажа готовых самолётов")
            if show_plant:
                ay = oy + oh
                vconn(63, ay, ay + 5, tip=True)
                label(66, ay + 0.5, "сборка по заказу")
                box(33, ay + 5, 144, 11, "Aero Vodochody — Чехия  (вне группы)",
                    "независимый завод: собирает самолёт по заказу за плату", dashed=True)
                bottom = ay + 5 + 11
            else:
                bottom = oy + oh
        else:
            box(24, oy, 78, oh, "Производственная компания — Польша или Болгария",
                "собственный завод: сборка самолёта внутри группы")
            box(108, oy, 78, oh, "SalesCo — Болгария",
                "военно-экспортная лицензия; продажа готовых самолётов")
            bottom = oy + oh
        self.set_y(bottom + 1.5)


