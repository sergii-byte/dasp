def h1(t):
    p = doc.add_paragraph()
    p.space_before = Pt(10)
    r = p.add_run(t); r.bold = True; r.font.size = Pt(15); r.font.color.rgb = NAVY
    pborder(p)
    return p

def h2(t):
    p = doc.add_paragraph()
    r = p.add_run(t); r.bold = True; r.font.size = Pt(11); r.font.color.rgb = NAVY
    p.paragraph_format.space_before = Pt(8); p.paragraph_format.space_after = Pt(2)
    return p

def pborder(p):
    pPr = p._p.get_or_add_pPr()
    pbdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single"); bottom.set(qn("w:sz"), "6")
    bottom.set(qn("w:space"), "2"); bottom.set(qn("w:color"), "142647")
    pbdr.append(bottom); pPr.append(pbdr)

def para(t, size=10.5):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(5)
    r = p.add_run(t); r.font.size = Pt(size)
    return p

def bullet(t):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.space_after = Pt(3)
    r = p.add_run(t); r.font.size = Pt(10)
    return p

def img(name):
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(4); p.paragraph_format.space_after = Pt(4)
    p.add_run().add_picture(f"{A}/assets_{name}.png", width=IMGW)

def caption(t):
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(t); r.italic = True; r.font.size = Pt(8); r.font.color.rgb = GREY
    p.paragraph_format.space_after = Pt(6)

def shade(cell, hexc):
    tcPr = cell._tc.get_or_add_tcPr()
    sh = OxmlElement("w:shd"); sh.set(qn("w:val"), "clear"); sh.set(qn("w:fill"), hexc)
    tcPr.append(sh)

def table(headers, rows, fs=8.5):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = "Table Grid"; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr = t.rows[0].cells
    for i, htext in enumerate(headers):
        shade(hdr[i], "142647")
        pr = hdr[i].paragraphs[0]; run = pr.add_run(htext)
        run.bold = True; run.font.size = Pt(fs); run.font.color.rgb = RGBColor(255, 255, 255)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            pr = cells[i].paragraphs[0]; run = pr.add_run(str(val))
            run.font.size = Pt(fs)
    doc.add_paragraph().paragraph_format.space_after = Pt(2)
    return t

