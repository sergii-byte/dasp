class PDF(FPDF):
    def __init__(self):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_auto_page_break(auto=True, margin=20)
        self.add_font("DejaVu", "", f"{FONT_DIR}/DejaVuSans.ttf")
        self.add_font("DejaVu", "B", f"{FONT_DIR}/DejaVuSans-Bold.ttf")
        self.add_font("DejaVu", "I", f"{FONT_DIR}/DejaVuSans.ttf")
        self.set_margins(20, 20, 20)

    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("DejaVu", "", 7.5)
        self.set_text_color(*GREY)
        self.set_y(10)
        self.cell(0, 5, "Независимое заключение по меморандуму STRIKER · L-259U", align="L")
        self.set_x(20)
        self.cell(0, 5, "Конфиденциально", align="R")
        self.set_draw_color(*LINE)
        self.line(20, 16, 190, 16)
        self.set_y(22)

    def footer(self):
        if self.page_no() == 1:
            return
        self.set_y(-15)
        self.set_draw_color(*LINE)
        self.line(20, self.get_y(), 190, self.get_y())
        self.set_font("DejaVu", "", 7.5)
        self.set_text_color(*GREY)
        self.cell(0, 8, f"Стр. {self.page_no()}", align="C")

    # ---- helpers ----
    def h1(self, num, text):
        self.ln(2)
        self.set_font("DejaVu", "B", 13)
        self.set_text_color(*NAVY)
        self.multi_cell(0, 7, f"{num}  {text}")
        self.set_draw_color(*NAVY)
        self.line(20, self.get_y()+1, 190, self.get_y()+1)
        self.ln(3)

    def h2(self, text):
        self.ln(1)
        self.set_font("DejaVu", "B", 10.5)
        self.set_text_color(*NAVY)
        self.multi_cell(0, 5.5, text)
        self.ln(1)

    def body(self, text):
        self.set_font("DejaVu", "", 9.5)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 5, text)
        self.ln(1)

    def bullet(self, text, color=None):
        self.set_font("DejaVu", "", 9.5)
        x = self.get_x()
        self.set_text_color(*(color or (30, 30, 30)))
        self.cell(5, 5, "▪")
        self.set_x(x + 5)
        self.multi_cell(0, 5, text)
        self.set_x(x)

    def tag(self, label, color):
        self.set_font("DejaVu", "B", 8)
        self.set_fill_color(*color)
        self.set_text_color(255, 255, 255)
        w = self.get_string_width(label) + 4
        self.cell(w, 5, label, align="C", fill=True)
        self.set_text_color(30, 30, 30)

