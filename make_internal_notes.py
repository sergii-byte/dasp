class PDF(FPDF):
    def __init__(self):
        super().__init__("P", "mm", "A4")
        self.set_auto_page_break(True, 18)
        self.add_font("D", "", f"{FD}/DejaVuSans.ttf")
        self.add_font("D", "B", f"{FD}/DejaVuSans-Bold.ttf")
        self.add_font("D", "I", f"{FD}/DejaVuSans.ttf")
        self.set_margins(18, 18, 18)
        self.cw = 174
    def header(self):
        if self.page_no() == 1: return
        self.set_font("D", "", 7.5); self.set_text_color(*GREY); self.set_y(9)
        self.cell(0, 5, "STRIKER · служебная записка")
        self.set_x(18); self.cell(0, 5, "ВНУТРЕННЕЕ — не для инвесторов", align="R")
        self.set_draw_color(*LINE); self.line(18, 15, 192, 15); self.set_y(20)
    def footer(self):
        if self.page_no() == 1: return
        self.set_y(-14); self.set_draw_color(*LINE); self.line(18, self.get_y(), 192, self.get_y())
        self.set_font("D", "", 7.5); self.set_text_color(*GREY)
        self.cell(0, 8, "Служебная записка к меморандуму о структуре")
        self.set_x(18); self.cell(0, 8, f"{self.page_no()}", align="R")
    def H1(self, t):
        if self.get_y() > 250: self.add_page()
        self.ln(2); self.set_font("D", "B", 14); self.set_text_color(*NAVY); self.set_x(18)
        self.multi_cell(self.cw, 7.5, t, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*NAVY); self.line(18, self.get_y()+1, 192, self.get_y()+1); self.ln(3.5)
    def H2(self, t):
        if self.get_y() > 255: self.add_page()
        self.ln(1.5); self.set_font("D", "B", 10.5); self.set_text_color(*NAVY); self.set_x(18)
        self.multi_cell(self.cw, 5.5, t, new_x="LMARGIN", new_y="NEXT"); self.ln(1)
    def P(self, t, s=9.5):
        self.set_font("D", "", s); self.set_text_color(*INK); self.set_x(18)
        self.multi_cell(self.cw, 5, t, new_x="LMARGIN", new_y="NEXT"); self.ln(1.2)
    def B(self, t):
        self.set_font("D", "", 9.5); self.set_text_color(*INK); self.set_x(18)
        self.cell(5, 5, "▪"); self.set_x(23)
        self.multi_cell(self.cw - 5, 5, t, new_x="LMARGIN", new_y="NEXT")
    def table(self, headers, widths, rows, fs=8):
        sc = self.cw / sum(widths); widths = [w*sc for w in widths]
        def hdr():
            self.set_font("D", "B", fs); self.set_fill_color(*NAVY); self.set_text_color(255,255,255); self.set_x(18)
            for w,h in zip(widths, headers): self.cell(w, 6.5, " "+h, fill=True)
            self.ln(6.5)
        hdr(); fill=False
        for row in rows:
            self.set_font("D", "", fs)
            lines=[self.multi_cell(w-2,4.3,str(c),dry_run=True,output="LINES") for w,c in zip(widths,row)]
            h=max(len(l) for l in lines)*4.3+2
            if self.get_y()+h>272: self.add_page(); hdr()
            x,y=18,self.get_y(); self.set_fill_color(*(LIGHT if fill else (255,255,255)))
            for w,c in zip(widths,row):
                cx,cy=self.get_x(),self.get_y(); self.rect(cx,cy,w,h,"F")
                self.set_xy(cx+1,cy+1); self.set_text_color(*INK)
                self.multi_cell(w-2,4.3,str(c),new_x="RIGHT",new_y="TOP",max_line_height=4.3)
                self.set_xy(cx+w,cy)
            self.set_draw_color(*LINE); self.line(x,y+h,x+sum(widths),y+h); self.set_xy(x,y+h); fill=not fill
        self.ln(2)

