def pborder(p):
    pPr = p._p.get_or_add_pPr(); pbdr = OxmlElement("w:pBdr"); b = OxmlElement("w:bottom")
    b.set(qn("w:val"), "single"); b.set(qn("w:sz"), "6"); b.set(qn("w:space"), "2"); b.set(qn("w:color"), "142647")
    pbdr.append(b); pPr.append(pbdr)
def shade_el(el, hexc):
    pPr = el.get_or_add_tcPr() if el.tag.endswith("}tc") else el.get_or_add_pPr()
    sh = OxmlElement("w:shd"); sh.set(qn("w:val"), "clear"); sh.set(qn("w:fill"), hexc); pPr.append(sh)
def h1(t):
    p = doc.add_paragraph(); p.paragraph_format.space_before = Pt(10)
    r = p.add_run(t); r.bold = True; r.font.size = Pt(15); r.font.color.rgb = NAVY; pborder(p); return p
def h2(t):
    p = doc.add_paragraph(); r = p.add_run(t); r.bold = True; r.font.size = Pt(11); r.font.color.rgb = NAVY
    p.paragraph_format.space_before = Pt(8); p.paragraph_format.space_after = Pt(2); return p
def para(t, size=10.5):
    p = doc.add_paragraph(); p.paragraph_format.space_after = Pt(5); r = p.add_run(t); r.font.size = Pt(size); return p
def bullet(t):
    p = doc.add_paragraph(style="List Bullet"); p.paragraph_format.space_after = Pt(3); r = p.add_run(t); r.font.size = Pt(10); return p
def img(name):
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(4); p.paragraph_format.space_after = Pt(4)
    p.add_run().add_picture(f"{A}/assets_{name}.png", width=IMGW)
def caption(t):
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(t); r.italic = True; r.font.size = Pt(8); r.font.color.rgb = GREY; p.paragraph_format.space_after = Pt(6)
def banner(label, title):
    p = doc.add_paragraph(); shade_el(p._p, "142647")
    p.paragraph_format.space_before = Pt(10); p.paragraph_format.space_after = Pt(8)
    r = p.add_run(label + "    "); r.font.size = Pt(10); r.font.color.rgb = RGBColor(0xAA, 0xBE, 0xDC)
    r2 = p.add_run(title); r2.bold = True; r2.font.size = Pt(15); r2.font.color.rgb = RGBColor(255, 255, 255)
def table(headers, rows, fs=8.5):
    t = doc.add_table(rows=1, cols=len(headers)); t.style = "Table Grid"; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, htext in enumerate(headers):
        c = t.rows[0].cells[i]; shade_el(c._tc, "142647")
        run = c.paragraphs[0].add_run(htext); run.bold = True; run.font.size = Pt(fs); run.font.color.rgb = RGBColor(255, 255, 255)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            run = cells[i].paragraphs[0].add_run(str(val)); run.font.size = Pt(fs)
    doc.add_paragraph().paragraph_format.space_after = Pt(2); return t

